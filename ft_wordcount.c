#include "libft.h"

int	ft_wordcount(char const *s, char c)
{
	int cnt;

	cnt = 0;
	while (*s)
	{
		while (*s == c)
			s++;
		while (*s && *s != c)
			s++;
		if (*(s - 1) != c)
			cnt++;
	}
	return (cnt);
}
